#include <stdio.h>

int main()
{
  int add = 1 + 3;
  int multiply = 3 * 4;
  int subtract = 4 - 2;
  int divide = 9 / 3;
  int modulo = 12 % 9;

  printf("Adding: %d\n", add);
  printf("Multiply: %d\n", multiply);
  printf("Divide: %d\n", divide);
  printf("Subtract: %d\n", subtract);
  printf("Module: %d\n", modulo);

  return 0;
}